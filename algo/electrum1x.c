void electrum1x_init() {
  int i, len;
  
  for (i = 0; i < MAX_HASH_VALUE; ++i) { min_table[i] = -1; }

  for (i = 0; i < TOTAL_KEYWORDS; ++i) {
    len = strlen(electrum1x_words[i]);
    min_table[electrum1x_phash(electrum1x_words[i], len)] = i;
    len_table[i] = len;
  }
}

short electrum1x_mhash(const char * str) {
  short ret;
  unsigned int phash, len;
  // get length of word
  len = strchrnul(str, ' ') - str;
  // check length of word
  if (len < MIN_WORD_LENGTH || len > MAX_WORD_LENGTH) { return -1; }
  // get and check hash value
  if ((phash = electrum1x_phash(str, len)) > MAX_HASH_VALUE) { return -1; }
  // check table
  if ((ret = min_table[phash]) < 0) { return -1; }
  // check for length match
  if (len != len_table[ret]) { return -1; }
  // check for full match
  if (memcmp(electrum1x_words[ret], str, len) != 0) { return -1; }
  return ret;
}

unsigned int electrum1x_decode(char * out, const char * mn) {
  char *ptr, *o = out;
  unsigned int tmp;
  int len = 0;
  short w1, w2, w3;

  out[0] = 0;

  // this parsing loop has undefined output on invalid seeds 
  while (1) {
    ptr = strchrnul(mn, ' ');
    if (!ptr[0]) {
      if (len == 0) { // this was the first word, assume hex input
        strncpy(out, mn, 256);
        return strlen(out); 
      } else {
        break;
      }
    }
    if ((w1 = electrum1x_mhash(mn)) < 0) { return out[0] = 0; }
    mn = ptr + 1;
  
    ptr = strchrnul(mn, ' ');
    if (!ptr[0]) { break; }
    if ((w2 = electrum1x_mhash(mn)) < 0) { return out[0] = 0; }
    mn = ptr + 1;
  
    ptr = strchrnul(mn, ' ');
    if ((w3 = electrum1x_mhash(mn)) < 0) { return out[0] = 0; }
    mn = ptr + 1;
  
    tmp = w1 +
         TOTAL_KEYWORDS * (((w2 - w1) + TOTAL_KEYWORDS) % TOTAL_KEYWORDS) +
         TOTAL_KEYWORDS * TOTAL_KEYWORDS * (((w3 - w2) + TOTAL_KEYWORDS ) % TOTAL_KEYWORDS); 
  
    sprintf(o, "%08x", tmp);

    len += 8; o += 8;

    if (!ptr[0]) break;
  }

  return len;
}

void electrum1x_stretch(char * out, const char * seed, int len) {
  SHA256_CTX ctx;
  unsigned char buf[512];
  int i;

  memcpy(buf, seed, len);
  memcpy(buf + len, seed, len);

  SHA256_Init(&ctx);
  SHA256_Update(&ctx, buf, len*2);
  SHA256_Final(buf, &ctx);

  memcpy(buf + 32, seed, len);

  for (i = 1; i < 100000; ++i) {
    SHA256_Init(&ctx);
    SHA256_Update(&ctx, buf, 32+len);
    SHA256_Final(buf, &ctx);
  }

  for (i = 0; i < 32; ++i) {
    sprintf(out+i*2, "%02x", buf[i]);
  }
}

